      <h2 style="margin-top:0;">Untuk melakukan Transaksi, silahkan ikuti langkah-langkah berikut:</h2>
      <ol>
        <li>
          Silahkan login terlebih dahulu sebagai pelanggan. Klik login di kanan atas menu navigasi dan isikan email dan password.
           Jika anda belum memiliki username dan password silahkan
          mendaftar secara manual di Rumah Makan Mahasiswa. Untuk mengetahui cara pendaftaran klik <a href="?page=panduan-daftar">disini</a>
          <img class="img img-responsive img-rounded" src="img\login.png" alt="login"><br>
        </li>
        <li>
          Pilih menu makanan atau minuman yang ingin dipesan. Klik pada gambar makanan atau minuman untuk
          melihat deskripsi dari menu yang dimaksud.
          <img class="img img-responsive img-rounded" src="img\view.png" alt="gambar"><br>
        </li>
        <li>
          Masukkan jumlah makanan yang ingin dipesan dan Klik <em>PESAN</em> untuk memesan.
          <img class="img img-responsive img-rounded" src="img\jumlah.png" alt="Jumlah pesan"><br>
        </li>
        <li>
          Pilih <em>TAB NOTA</em> di bagian navigasi atas untuk melihat daftar pesanan yang dipesan.
        </li>
        <li>
          Edit alamat dan no handphone jika tidak sesuai dengan alamat dan no handphone anda saat ini.
          <img class="img img-responsive img-rounded" src="img\nota.png" alt="Nota"><br>
        </li>
        <li>
          Klik <em>Kirim</em> untuk melanjutkan pemesanan makanan dan atau minuman. Klik <em>BATAL</em> untuk membatalkan proses pemesanan
        </li>
        <li>
          Anda akan mendapatkan pesan selamat dari RMM.
        </li>
        <li>
          Karyawan RMM akan mengantar pesanan anda ke alamat yang tertera. Tunggu 15 - 60 menit sampai pesanan anda sampai
        </li>
        <li>
          Setelah pesanan sampai, bayarlah uang pembayaran sesuai nota ke karyawan RMM
        </li>
        <li>
          Selamat menikmati makanan anda
        </li>
